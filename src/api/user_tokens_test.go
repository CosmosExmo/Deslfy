func randomUserToken(owner string) db.UserToken {
	return db.UserToken{
		ID:        util.RandomInt(10, 50),
		Owner:     owner,
		Token:     util.RandomString(64),
		ExpireAt:  time.Now().Add(24 * time.Hour),
		CreatedAt: time.Now(),
	}
}
